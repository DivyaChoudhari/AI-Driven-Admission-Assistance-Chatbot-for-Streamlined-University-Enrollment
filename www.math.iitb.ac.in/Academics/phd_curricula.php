            <p style="font-size:16px;">

				</p><p style="font-size:16px;">Students in the PhD program have to fulfill the Qualifying Examination requirement within 3 semesters of joining. Please read further for details.</p>

				<p style="font-size:16px;">Qualifying Examinations are conducted in the following seven subjects twice every year: (1) Algebra, (2) Analysis, (3) Geometry and Topology, (4) Differential Equations, (5) Probability, (6) Statistics and (7) Combinatorics and Theoretical Computer Science. Typically these exams are conducted during 1-15 July and 15-31 December each year and the results are declared by 21st July and 7th January, respectively. Each exam is out of 100 and the pass mark is 60.</p>

				<p style="font-size:16px;">The student may attempt Qualifying Examinations in subjects of his/her choice. In order to fulfill the Qualifying Examination requirement the student has to pass in any two Qualifying Examinations within 3 semesters of joining the PhD program.</p>

				<p style="font-size:16px;">In case a student fails to complete the Qualifying Examination requirement at the end of his/her third semester, then he/she has the option of transferring to M.Phil. program, by continuing for about a semester, so as to complete the requirements for an M.Phil.degree.</p>

				<p style="font-size:16px;">The student may register with a Ph.D. thesis supervisor after the successful fulfillment of the coursework and Qualifying Examination requirement. Until that time the Faculty Advisor shall be the guide for all official purposes.</p>


