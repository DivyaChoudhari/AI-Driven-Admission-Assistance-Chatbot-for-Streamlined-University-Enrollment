            <h2 class=" text-center"><small><u><b>Overview</b></u></small></h2>
<p style="font-size:16px;">Ph.D. PROGRAMME IN MATHEMATICS offers an exciting and unique opportunity to students for pursuing research in the following areas:</p>
<ul style="font-size:16px;">
<li>Pure Mathematics</li>
<li>Applied Mathematics & Scientific Computing</li>
<li>Probability & Statistics</li>
<li>Theoretical Computer Science</li>
</ul>

<p style="font-size:16px;">Under this programme students undergo a substantial amount of relevant course work consisting of advance topics in Aglebra, Analysis, Topology, Numerical Analysis, Solid & Fluid Mechanics and Statistics, followed by research work under the supervision of an Advisor, who is decided by the Department, taking into account the aptitude, the needs and the preferences of the student.</p>

            <h2 class=" text-center"><small><u><b>Salient Features</b></u></small></h2>
<ul style="font-size:16px;">
<li>Nurtures creative and analytical abilities of scholars.</li>
<li>Provides flexible approach for self-study and seminar courses in different areas.</li>
<li>Encourages interdisciplinary research for bridging the gap between theoretical and applied areas.</li>
<li>Cultivates communication skills through seminars and tutorials.</li>
<li>Offers opportunity to engage in developmental research work.</li>
</ul>

            <h2 class=" text-center"><small><u><b>About the Department</b></u></small></h2>
<p style="font-size:16px;">The Department of Mathematics is well recognised for teaching and research. It has a large faculty with research interests covering a wide range of fields:</p>
<ul style="font-size:16px;">
<li>Algebra, Combinatorics, Topology and Geometry</li>
<li>Functional Analysis, Harmonic Analysis, Partial Differential Equations and Systems & Control.</li>
<li>Numerical Analysis & Scientific Computing.</li>
<li>Statistical Inference, Applied Probability, Statistical Quality Control and Biostatistics.</li>
<li>Algorithms & Complexity and Combinatorial Optimization,</li>
</ul>

<p style="font-size:16px;">Collabortive research with other Science and Engineering Departments of the Institute is encouraged. Faculty members undertake projects sponsored by organizations such as National Board for Higher Mathematics, Indian National Science Academy, Board of Research in Nuclear Science, Council of Scientific & Industrial Research, Department of Science and Technology, Department of Bio-Technology and Indian Council of Medical Research etc. A strong group of Industrial Mathematics has evolved in the Department for providing Industry-Academic linkage.</p>

