<p align="justify" style="font-size:16px;">Mathematics is one of the oldest fields of human inquiry and integral to our culture and heritage. Its grand questions have been motivated as much by our fascination with its inherent beauty and complexity as by the practical considerations of architecture, science, and technology. In the modern world, mathematics thrives in its central role as the language of traditional science, while reaching further into the realms of engineering, finance, and social sciences.</p>

<p align="justify" style="font-size:16px;">The B.S. in Mathematics at IIT Bombay is designed to build a strong foundation in the initial semesters, while encouraging students to pursue more targeted interests in the second half of the programme through advanced elective courses within the Department of Mathematics. The curriculum also includes basic science courses and incorporates elective courses from the science, engineering, and humanities disciplines, allowing for considerable flexibility and academic freedom. The entrance to this programme is through JEE (Advanced). <!--Details of entry to this programme can be found <a data-toggle="tab" href="#entry">here.</a>---></p>

<p align="justify" style="font-size:16px;">Students are required to complete basic courses in algebra, combinatorics, calculus and analysis, differential equations, and probability. More advanced courses in these subjects, as well as in geometry, topology, number theory, numerical analysis, statistics, and theoretical computer science will be on offer. Motivated students will benefit from the presence of active research groups in all of these areas. The Honours programme promotes deeper scholarship within mathematics while the Minor programme enables the pursuit of interests in other departments.</p>
