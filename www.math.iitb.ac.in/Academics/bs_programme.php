<p align="justify" style="font-size:16px;">The B.S. in Mathematics is designed to equip its graduates with a wide range of mathematical skills and knowledge and provide exposure to science, engineering and other subjects. They will be well-placed to make significant contributions to academia, industry and other important institutions in our society.</p>



